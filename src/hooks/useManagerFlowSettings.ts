import { useState, useEffect, useCallback } from 'react';
import { supabase } from '@/integrations/supabase/client';
import { useAuth } from '@/contexts/AuthContext';

export interface ManagerFlowSettings {
  id: string;
  manager_id: string;
  manter_tipo_selecionado: boolean;
  manter_waiter_selecionado: boolean;
  foco_apos_salvar: 'valor' | 'motivo';
  ativo: boolean;
  created_at: string;
  updated_at: string;
}

export function useManagerFlowSettings() {
  const { user } = useAuth();
  const [settings, setSettings] = useState<ManagerFlowSettings | null>(null);
  const [isLoading, setIsLoading] = useState(true);
  const [isSaving, setIsSaving] = useState(false);
  const [error, setError] = useState<string | null>(null);
  const [refreshKey, setRefreshKey] = useState(0); // For√ßar recarregamento

  // Carregar configura√ß√µes do gerente logado
  const loadSettings = useCallback(async () => {
    if (!user?.id) {
      setIsLoading(false);
      return;
    }

    try {
      setIsLoading(true);
      setError(null);

      const { data, error: fetchError } = await supabase
        .from('manager_flow_settings')
        .select('*')
        .eq('manager_id', user.id)
        .eq('ativo', true)
        .maybeSingle();

      if (fetchError) {
        console.error('‚ùå Erro ao carregar configura√ß√µes:', fetchError);
        throw fetchError;
      }

      console.log('üì• Configura√ß√µes carregadas:', data);
      setSettings(data);
    } catch (err) {
      console.error('Erro ao carregar configura√ß√µes de fluxo:', err);
      setError('Erro ao carregar configura√ß√µes de fluxo');
    } finally {
      setIsLoading(false);
    }
  }, [user?.id]);

  // Salvar configura√ß√µes
  const saveSettings = useCallback(async (newSettings: Partial<ManagerFlowSettings>) => {
    if (!user?.id) {
      setError('Usu√°rio n√£o autenticado');
      return false;
    }

    try {
      setIsSaving(true);
      setError(null);

      // Se j√° existe configura√ß√£o, atualizar
      if (settings?.id) {
        console.log('üîÑ Atualizando configura√ß√µes existentes:', { settingsId: settings.id, newSettings });
        
        const { error: updateError } = await supabase
          .from('manager_flow_settings')
          .update({
            ...newSettings,
            updated_at: new Date().toISOString(),
            updated_by: user.id
          })
          .eq('id', settings.id);

        if (updateError) {
          console.error('‚ùå Erro ao atualizar configura√ß√µes:', updateError);
          throw updateError;
        }
        
        console.log('‚úÖ Configura√ß√µes atualizadas com sucesso');
      } else {
        // Se n√£o existe, criar nova
        const { error: insertError } = await supabase
          .from('manager_flow_settings')
          .insert({
            manager_id: user.id,
            manter_tipo_selecionado: newSettings.manter_tipo_selecionado ?? true,
            manter_waiter_selecionado: newSettings.manter_waiter_selecionado ?? false,
            foco_apos_salvar: newSettings.foco_apos_salvar ?? 'valor',
            ativo: true,
            created_by: user.id,
            updated_by: user.id
          });

        if (insertError) throw insertError;
      }

      // For√ßar recarregamento das configura√ß√µes
      console.log('üîÑ For√ßando recarregamento das configura√ß√µes...');
      setRefreshKey(prev => prev + 1);
      await loadSettings();
      console.log('‚úÖ Configura√ß√µes recarregadas ap√≥s salvamento');
      return true;
    } catch (err) {
      console.error('Erro ao salvar configura√ß√µes de fluxo:', err);
      setError('Erro ao salvar configura√ß√µes de fluxo');
      return false;
    } finally {
      setIsSaving(false);
    }
  }, [user?.id, settings?.id]); // Remover loadSettings da depend√™ncia

  // Carregar configura√ß√µes quando o usu√°rio estiver autenticado ou refreshKey mudar
  useEffect(() => {
    if (user?.id) {
      console.log('üîÑ Hook: Carregando configura√ß√µes para usu√°rio:', user.id, 'refreshKey:', refreshKey);
      loadSettings();
    }
  }, [user?.id, refreshKey]); // Incluir refreshKey para for√ßar recarregamento

  // For√ßar recarregamento quando settings mudarem
  useEffect(() => {
    console.log('üîÑ Hook: Settings mudaram:', settings);
  }, [settings]);

  return {
    settings,
    isLoading,
    isSaving,
    error,
    saveSettings,
    refreshSettings: loadSettings
  };
}
